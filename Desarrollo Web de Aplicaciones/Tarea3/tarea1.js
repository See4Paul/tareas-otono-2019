function regionComunaSetter(regionId,comunaId){

	var iRegion=0;
	var htmlRegion = '<option value="sin-region">Seleccione región</option><option value="sin-region">--</option>';
	var htmlComunas = '<option value="sin-comuna">Seleccione comuna</option><option value="sin-comuna">--</option>';
	

	jQuery.each(RegionesYcomunas.regiones, function () {
		htmlRegion = htmlRegion + '<option value="' + RegionesYcomunas.regiones[iRegion].NombreRegion + '">' + RegionesYcomunas.regiones[iRegion].NombreRegion + '</option>';
		iRegion++;
	});

	jQuery('#'+regionId).html(htmlRegion);
	jQuery('#'+comunaId).html(htmlComunas);

	jQuery('#'+regionId).change(function () {
		var iRegiones = 0;
		var valorRegion = jQuery(this).val();
		//RESET htmlComuna
		//$("#comunas").replaceWith(comunasInicial.clone());
		//contador_comunas=0;
		//
		var htmlComuna = '<option value="sin-comuna">Seleccione comuna</option><option value="sin-comuna">--</option>';
		jQuery.each(RegionesYcomunas.regiones, function () {
			if (RegionesYcomunas.regiones[iRegiones].NombreRegion == valorRegion) {
				var iComunas = 0;
				jQuery.each(RegionesYcomunas.regiones[iRegiones].comunas, function () {
					htmlComuna = htmlComuna + '<option value="' + RegionesYcomunas.regiones[iRegiones].comunas[iComunas] + '">' + RegionesYcomunas.regiones[iRegiones].comunas[iComunas] + '</option>';
					iComunas++;
				});
			}
			iRegiones++;
		});
		jQuery('#'+comunaId).html(htmlComuna);
	});
	jQuery('#'+comunaId).change(function () {
		if (jQuery(this).val() == 'sin-region') {
			alert('selecciones Región');
		} else if (jQuery(this).val() == 'sin-comuna') {
			alert('selecciones Comuna');
		}
	});
	jQuery('#'+regionId).change(function () {
		if (jQuery(this).val() == 'sin-region') {
			alert('selecciones Región');
		}
		});
}

