function nameValidator(container) {
	console.log("Hola uwu")
	var nombre  = document.getElementById(container).value;
	console.log(container + " : "+ nombre);
	var regex = /^[A-Z][a-z]+\s[A-Z][a-z]+$/;
	if (nombre.length < 3) {
		alert("Su nombre debe ser más largo porfavor!")
		return false;
	}

	if (nombre.length > 80) {
			alert("Su nombre no debe exceder más de 80 carácteres ");
			return false;
	}
	if (!regex.test(nombre)) {
			alert(("Su nombre solo puede contener letras"   ));
			return false;
	}
	return true;
}

